package id.its.akademik.domain;

public class MKLanggarPrasyarat {

	private String id;
	private String nama;
	private String idPrasyarat;
	private String namaPrasyarat;
	private String keterangan;
	
	public MKLanggarPrasyarat() {
		
	}

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getNama() {
		return nama;
	}

	public void setNama(String nama) {
		this.nama = nama;
	}

	public String getKeterangan() {
		return keterangan;
	}

	public void setKeterangan(String keterangan) {
		this.keterangan = keterangan;
	}

	public String getIdPrasyarat() {
		return idPrasyarat;
	}

	public void setIdPrasyarat(String idPrasyarat) {
		this.idPrasyarat = idPrasyarat;
	}

	public String getNamaPrasyarat() {
		return namaPrasyarat;
	}

	public void setNamaPrasyarat(String namaPrasyarat) {
		this.namaPrasyarat = namaPrasyarat;
	}
	
}
