package id.its.akademik.domain;

public class ProdiAjar {

	private String kodeJur;
	private String kodeProdi;
	private String prodiBaru;
	private String fakultasBaru;
	private String fakultasId;

	public String getKodeJur() {
		return kodeJur;
	}

	public void setKodeJur(String kodeJur) {
		this.kodeJur = kodeJur;
	}

	public String getKodeProdi() {
		return kodeProdi;
	}

	public void setKodeProdi(String kodeProdi) {
		this.kodeProdi = kodeProdi;
	}

	public String getProdiBaru() {
		return prodiBaru;
	}

	public void setProdiBaru(String prodiBaru) {
		this.prodiBaru = prodiBaru;
	}

	public String getFakultasBaru() {
		return fakultasBaru;
	}

	public void setFakultasBaru(String fakultasBaru) {
		this.fakultasBaru = fakultasBaru;
	}

	public String getFakultasId() {
		return fakultasId;
	}

	public void setFakultasId(String fakultasId) {
		this.fakultasId = fakultasId;
	}

}
