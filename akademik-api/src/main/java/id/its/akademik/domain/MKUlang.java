package id.its.akademik.domain;

public class MKUlang {

	private Integer semester;
	private String id;
	private String nama;
	private Integer sks;
	private String nilaiHuruf;
	private Integer tahunAmbil;
	private Integer semesterAmbil;
	
	public MKUlang() {
		
	}

	public Integer getSemester() {
		return semester;
	}

	public void setSemester(Integer semester) {
		this.semester = semester;
	}

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getNama() {
		return nama;
	}

	public void setNama(String nama) {
		this.nama = nama;
	}

	public Integer getSks() {
		return sks;
	}

	public void setSks(Integer sks) {
		this.sks = sks;
	}

	public String getNilaiHuruf() {
		return nilaiHuruf;
	}

	public void setNilaiHuruf(String nilaiHuruf) {
		this.nilaiHuruf = nilaiHuruf;
	}

	public Integer getTahunAmbil() {
		return tahunAmbil;
	}

	public void setTahunAmbil(Integer tahunAmbil) {
		this.tahunAmbil = tahunAmbil;
	}

	public Integer getSemesterAmbil() {
		return semesterAmbil;
	}

	public void setSemesterAmbil(Integer semesterAmbil) {
		this.semesterAmbil = semesterAmbil;
	}
	
	
	
}
