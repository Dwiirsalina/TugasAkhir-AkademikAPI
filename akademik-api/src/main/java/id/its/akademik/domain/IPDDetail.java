package id.its.akademik.domain;

public class IPDDetail {

	private Integer jumlahPengisiIpm;
	private Integer jumlahRespondenIpd;
	private Double ipd;
	private Double ipm;
	private Double persentasePengisiIpm;
	private Double persentasePengisiIpd;

	public Integer getJumlahPengisiIpm() {
		return jumlahPengisiIpm;
	}

	public void setJumlahPengisiIpm(Integer jumlahPengisiIpm) {
		this.jumlahPengisiIpm = jumlahPengisiIpm;
	}

	public Integer getJumlahRespondenIpd() {
		return jumlahRespondenIpd;
	}

	public void setJumlahRespondenIpd(Integer jumlahRespondenIpd) {
		this.jumlahRespondenIpd = jumlahRespondenIpd;
	}

	public Double getIpd() {
		return ipd;
	}

	public void setIpd(Double ipd) {
		this.ipd = ipd;
	}

	public Double getIpm() {
		return ipm;
	}

	public void setIpm(Double ipm) {
		this.ipm = ipm;
	}

	public Double getPersentasePengisiIpm() {
		return persentasePengisiIpm;
	}

	public void setPersentasePengisiIpm(Double persentasePengisiIpm) {
		this.persentasePengisiIpm = persentasePengisiIpm;
	}

	public Double getPersentasePengisiIpd() {
		return persentasePengisiIpd;
	}

	public void setPersentasePengisiIpd(Double persentasePengisiIpd) {
		this.persentasePengisiIpd = persentasePengisiIpd;
	}

}
